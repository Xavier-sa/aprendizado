saiyan_power = 9000  
kamehameha_damage = 1500.75 


total_power = saiyan_power + kamehameha_damage
average_power = saiyan_power / 2
power_squared = saiyan_power ** 2
remaining_power = saiyan_power % 1000

# Exibindo os resultados
print(f"Poder do Saiyan: {saiyan_power}")
print(f"Dano do Kamehameha: {kamehameha_damage}")
print(f"Total de Poder: {total_power}")
print(f"Poder Médio: {average_power}")
print(f"Poder ao Quadrado: {power_squared}")
print(f"Restante do Poder: {remaining_power}")

