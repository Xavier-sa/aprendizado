def operadores_basicos():
    """
    Função que demonstra o uso de operadores básicos em Python.
    """
    pass
    a = 10

    b = 3

    soma = a + b # Saída: 13

    subtracao = a - b # Saída: 7

    multiplicacao = a * b # Saída: 30

    divisao = a / b # Saída: 3.333...

    resto = a % b # Saída: 1 (resto da divisão)

    print(f"Soma: \n{soma}, Subtração:\n {subtracao}, Multiplicação:\n {multiplicacao}, Divisão:\n{divisao}, Resto:\n {resto}")

operadores_basicos()